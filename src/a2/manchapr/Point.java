package a2.manchapr;

public class Point {
    private double w,z;

    public void setW(double w) {
        this.w = w;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public double getW() {
        return w;
    }

    public double getZ() {
        return z;
    }
    Point() {
        w=0;
        z=0;
    }
    
}
